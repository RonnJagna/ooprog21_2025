public class Comments {
    public static void main(String[] args) {
        // This is a single-line comment
        System.out.println("Hello! This line is after a single-line comment.");

        /*
         * This is a multi-line comment.
         * It can span across multiple lines.
         */
        System.out.println("This line is after a multi-line comment.");

        /**
         * This is a documentation comment (Javadoc).
         * It's usually used to describe classes, methods, or variables.
         */
        System.out.println("This line is after a documentation comment.");
    }
}
